// calculates bitwise 'AND' of given two inputs x and y
function bitwiseAnd(x: bigint, y: bigint): bigint {
  return x & y;
}

// calculates bitwise 'OR' of given two inputs x and y
function bitwiseOr(x: bigint, y: bigint): bigint {
  return x | y;
}

// calculates bitwise 'XOR' of given two inputs x and y
function bitwiseXor(x: bigint, y: bigint): bigint {
  return x ^ y;
}
